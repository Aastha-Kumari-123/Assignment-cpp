// Program to demonstrate arithmetic operators...
//arithmetic operators in cpp are +,-,*,/,%,++,--     
#include<iostream>
using namespace std;
int main()
{
  int a,b;
  cout<<"enter two numbers: ";
  cin>>a>>b;
  cout<<"addition of a and b is: "<<a+b<<endl;
  cout<<"substraction of a and b is: "<<a-b<<endl;
  cout<<"multiplication of a and b is: "<<a*b<<endl;
  cout<<"a divided by b is:"<<a/b<<endl;
  cout<<"remainder of a/b is: "<<a%b<<endl;
  return 0;
}